COMMENT ON COLUMN "NTTDATA_ORACLE_AUTHORS"."SURNAME" IS 'APELLIDO/S DEL AUTOR';
COMMENT ON COLUMN "NTTDATA_ORACLE_AUTHORS"."BIRTH_DATE" IS 'FECHA DE NACIMIENTO DEL AUTOR';
