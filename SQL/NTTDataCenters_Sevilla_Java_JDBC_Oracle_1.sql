-- Creacion tabla de autores.

CREATE TABLE NTTDATA_ORACLE_AUTHORS (

    ID INT NOT NULL,
    NAME VARCHAR(100),
    SURNAME VARCHAR(200),
    BIRTH_DATE DATE,
    COUNTRY VARCHAR(200),
    
    CONSTRAINT PK_ID_AUTHORS PRIMARY KEY (ID)

);

-- Descripcion de las columnas de la tabla autores.

COMMENT ON COLUMN "NTTDATA_ORACLE_AUTHORS"."ID" IS 'ID DEL AUTOR';
